const SESS_STEP    = 'epp_demo_step';
const SESS_PLAYING = 'epp_demo_playing';

const BLOCKS = Array.from(new Map(STEPS.map(s => [s.block, s])).values()).map(s => s.block);

export function DemoRunner({ onClose }: { onClose: () => void }) {
  const router   = useRouter();
  const { changeStatus } = useAppStore();
  const [step, setStep]       = useState(() =>
    typeof window !== 'undefined' ? Number(sessionStorage.getItem(SESS_STEP) ?? 0) : 0
  );
  const [playing, setPlaying] = useState(() =>
    typeof window !== 'undefined' ? sessionStorage.getItem(SESS_PLAYING) === 'true' : false
  );
  const [done, setDone]       = useState(false);
  const [showToc, setShowToc] = useState(false);
  const timerRef = useRef<ReturnType<typeof setTimeout> | null>(null);
  const stepRef  = useRef(step);
  stepRef.current = step;

  const cur    = STEPS[step] ?? STEPS[0];
  const isLast = step === STEPS.length - 1;
  const pct    = Math.round(((step + 1) / STEPS.length) * 100);

  useEffect(() => {
    sessionStorage.setItem(SESS_STEP, String(step));
    sessionStorage.setItem('epp_demo_open', 'true');
  }, [step]);
  useEffect(() => { sessionStorage.setItem(SESS_PLAYING, String(playing)); }, [playing]);

  const navigate = useCallback((route: string) => { router.push(route); }, [router]);

  const goTo = useCallback((target: number) => {
    if (timerRef.current) clearTimeout(timerRef.current);
    const s = STEPS[target];
    if (s.newStatus) changeStatus('p001', s.newStatus, 'u_shv', 'Швецов К.Е.');
    setStep(target);
    setShowToc(false);
    navigate(s.route);
  }, [changeStatus, navigate]);

  const goNext = useCallback(() => {
    if (timerRef.current) clearTimeout(timerRef.current);
    const cur = stepRef.current;
    if (cur >= STEPS.length - 1) {
      setDone(true); setPlaying(false);
      sessionStorage.removeItem(SESS_STEP);
      sessionStorage.removeItem(SESS_PLAYING);
      return;
    }
    const next = cur + 1;
    const s = STEPS[next];
    if (STEPS[cur].newStatus) changeStatus('p001', STEPS[cur].newStatus!, 'u_shv', 'Швецов К.Е.');
    setStep(next);
    navigate(s.route);
  }, [changeStatus, navigate]);

  const goPrev = useCallback(() => {
    if (step === 0) return;
    if (timerRef.current) clearTimeout(timerRef.current);
    const prev = step - 1;
    setStep(prev);
    navigate(STEPS[prev].route);
  }, [step, navigate]);

  useEffect(() => {
    if (!playing || done) return;
    timerRef.current = setTimeout(goNext, cur.duration);
    return () => { if (timerRef.current) clearTimeout(timerRef.current); };
  }, [playing, done, step, goNext, cur.duration]);

  useEffect(() => {
    const h = (e: KeyboardEvent) => {
      if (['INPUT','TEXTAREA','SELECT'].includes((e.target as HTMLElement).tagName)) return;
      if (e.key === 'ArrowRight' || e.key === 'Enter') goNext();
      else if (e.key === 'ArrowLeft') goPrev();
      else if (e.key === 'Escape') { if (showToc) setShowToc(false); else handleClose(); }
    };
    window.addEventListener('keydown', h);
    return () => window.removeEventListener('keydown', h);
  });

  const handleClose = () => {
    if (timerRef.current) clearTimeout(timerRef.current);
    sessionStorage.removeItem(SESS_STEP);
    sessionStorage.removeItem(SESS_PLAYING);
    sessionStorage.removeItem('epp_demo_open');
    onClose();
  };

  return (
    <>
      {/* Оглавление */}
      {showToc && (
        <div className="fixed inset-0 z-[9985]" onClick={() => setShowToc(false)}>
          <div className="absolute right-0 top-0 bottom-0 w-72 bg-white shadow-2xl border-l border-gray-200 flex flex-col"
            onClick={e => e.stopPropagation()}>
            <div className="px-4 py-3 border-b border-gray-100 flex items-center justify-between"
              style={{ background: '#003087' }}>
              <div>
                <div className="text-xs font-bold text-white">Содержание демонстрации</div>
                <div className="text-xs text-blue-200 opacity-70">{STEPS.length} шагов · 14 блоков · полный цикл 44-ФЗ</div>
              </div>
              <button onClick={() => setShowToc(false)} className="text-white opacity-60 hover:opacity-100 p-1">
                <X size={14}/>
              </button>
            </div>
            <div className="flex-1 overflow-y-auto p-2">
              {BLOCKS.map(block => {
                const blockSteps = STEPS.filter(s => s.block === block);
                const firstIdx   = STEPS.findIndex(s => s.block === block);
                const isCurrentBlock = cur.block === block;
                return (
                  <div key={block} className="mb-1">
                    <div className={`text-xs font-bold px-2 py-1.5 rounded mb-0.5 ${isCurrentBlock ? 'text-blue-700 bg-blue-50' : 'text-gray-400'}`}>
                      {block}
                    </div>
                    {blockSteps.map((s, i) => {
                      const idx = firstIdx + i;
                      return (
                        <button key={idx} onClick={() => goTo(idx)}
                          className={`w-full flex items-start gap-2 px-3 py-1.5 rounded text-left mb-0.5 transition-colors ${
                            idx === step ? 'bg-blue-600 text-white' :
                            idx < step   ? 'text-green-700 hover:bg-green-50' :
                                           'text-gray-600 hover:bg-gray-50'
                          }`}>
                          <span className="text-xs font-mono flex-shrink-0 mt-0.5">
                            {idx < step ? '✓' : String(idx + 1).padStart(2, '0')}
                          </span>
                          <span className="text-xs leading-snug">{s.title}</span>
                        </button>
                      );
                    })}
                  </div>
                );
              })}
            </div>
          </div>
        </div>
      )}

      {/* Панель снизу */}
      <div className="fixed inset-x-0 bottom-0 z-[9990]">
        {/* Прогресс */}
        <div style={{ height: 3, background: '#1e3a6e' }}>
          <div style={{ height: 3, width: `${pct}%`, background: 'linear-gradient(90deg, #4d9fff, #0050b3)', transition: 'width 0.5s' }}/>
        </div>

        <div style={{ background: '#fff', borderTop: '2px solid #003087', boxShadow: '0 -4px 20px rgba(0,48,135,0.12)' }}>
          <div style={{ maxWidth: 1100, margin: '0 auto' }}>
            {done ? (
              <div className="px-5 py-3 flex items-center justify-between gap-4">
                <div className="flex items-center gap-3">
                  <span className="text-2xl">🏆</span>
                  <div>
                    <div className="text-sm font-bold text-gray-800">Полный цикл закупки завершён!</div>
                    <div className="text-xs text-gray-500">{STEPS.length} шагов · 14 блоков · от служебной записки до архива · соответствие 44-ФЗ на каждом этапе</div>
                  </div>
                </div>
                <div className="flex gap-2">
                  <button onClick={() => { goTo(0); setDone(false); setPlaying(false); }}
                    className="gov-btn gov-btn-ghost gov-btn-sm">🔄 Сначала</button>
                  <button onClick={handleClose} className="gov-btn gov-btn-primary gov-btn-sm">
                    <CheckCircle size={13}/> Завершить
                  </button>
                </div>
              </div>
            ) : (
              <div className="px-4 py-2 flex items-start gap-4">
                {/* Контент */}
                <div className="flex-1 min-w-0">
                  {/* Мета строка */}
                  <div className="flex items-center gap-2 mb-1 flex-wrap">
                    <span className="text-xs font-bold text-white px-2 py-0.5 rounded-full flex-shrink-0"
                      style={{ background: '#003087' }}>
                      {step + 1} / {STEPS.length}
                    </span>
                    <span className="text-xs font-bold text-blue-600 flex-shrink-0">{cur.block}</span>
                    <span className="text-xs text-gray-400">·</span>
                    <span className="text-xs text-gray-500 flex-shrink-0">{cur.role}</span>
                    {cur.law && (
                      <>
                        <span className="text-xs text-gray-400">·</span>
                        <span className="text-xs text-indigo-600 flex-shrink-0 font-medium">{cur.law}</span>
                      </>
                    )}
                    {playing && <span className="text-xs text-orange-500 animate-pulse ml-auto flex-shrink-0">⏱ авто</span>}
                  </div>

                  {/* Заголовок */}
                  <div className="text-sm font-bold text-gray-900 mb-1.5 leading-tight">{cur.title}</div>

                  {/* Что / Зачем / Результат */}
                  <div className="grid grid-cols-1 sm:grid-cols-3 gap-x-4 gap-y-0.5">
                    <div className="text-xs text-gray-600 leading-relaxed sm:col-span-1">
                      <span className="font-semibold text-gray-700">Что: </span>{cur.what.slice(0, 180)}{cur.what.length > 180 ? '…' : ''}
                    </div>
                    {cur.why && (
                      <div className="text-xs text-blue-700 leading-relaxed">
                        <span className="font-semibold">Зачем: </span>{cur.why.slice(0, 140)}{cur.why.length > 140 ? '…' : ''}
                      </div>
                    )}
                    <div className="text-xs text-green-700 leading-relaxed">
                      <span className="font-semibold">Результат: </span>{cur.result}
                    </div>
                  </div>

                  {/* Подсказка */}
                  {cur.highlight && (
                    <div className="mt-1 text-xs text-amber-700 flex items-center gap-1">
                      <Zap size={10} className="flex-shrink-0"/>
                      <span>{cur.highlight}</span>
                    </div>
                  )}
                </div>

                {/* Управление */}
                <div className="flex flex-col items-end gap-1.5 flex-shrink-0">
                  <div className="flex items-center gap-1.5">
                    <button onClick={goPrev} disabled={step === 0}
                      className="p-1.5 text-gray-400 hover:text-gray-700 disabled:opacity-25 rounded hover:bg-gray-100 transition-colors">
                      <ChevronLeft size={16}/>
                    </button>
                    <button onClick={() => setPlaying(p => !p)}
                      className={`flex items-center gap-1 px-3 py-1.5 text-xs font-bold rounded-lg transition-colors ${
                        playing ? 'bg-orange-500 hover:bg-orange-600 text-white' : 'bg-gray-100 hover:bg-gray-200 text-gray-700'
                      }`}>
                      {playing ? <><Pause size={12}/> Пауза</> : <><Play size={12}/> Авто</>}
                    </button>
                    <button onClick={goNext}
                      className="flex items-center gap-1.5 px-5 py-1.5 text-sm font-bold text-white rounded-lg transition-colors shadow-sm"
                      style={{ background: '#003087' }}>
                      {isLast ? <><CheckCircle size={13}/> Готово</> : <>Далее <ChevronRight size={13}/></>}
                    </button>
                    <button onClick={() => setShowToc(t => !t)}
                      className={`p-1.5 rounded hover:bg-gray-100 transition-colors ${showToc ? 'text-blue-600' : 'text-gray-400 hover:text-gray-700'}`}
                      title="Содержание">
                      <BookOpen size={15}/>
                    </button>
                    <button onClick={handleClose}
                      className="p-1.5 text-gray-300 hover:text-gray-600 rounded hover:bg-gray-100 transition-colors">
                      <X size={14}/>
                    </button>
                  </div>

                  {/* Прогресс-точки */}
                  <div className="flex gap-0.5 flex-wrap justify-end max-w-52">
                    {STEPS.map((_, i) => (
                      <button key={i} onClick={() => goTo(i)}
                        className="rounded-full transition-all duration-200"
                        style={{
                          width: i === step ? 16 : 6,
                          height: 6,
                          background: i === step ? '#003087' : i < step ? '#4ade80' : '#e5e7eb'
                        }}
                      />
                    ))}
                  </div>
                </div>
              </div>
            )}
          </div>
        </div>
      </div>
    </>
  );
}

